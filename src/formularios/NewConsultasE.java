    private void btnActActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActActionPerformed
        conectar cc = new conectar();
        Connection cn = cc.conexion();
        try {
            PreparedStatement pps = cn.prepareStatement("UPDATE consultas SET cod_con='" + txtcodcon.getText() + "',fec_rev='" + txtfecini.getText() + "',"
                    + "pro_con='" + txtpro.getText() + "',sol_con='" + txtsol.getText() + "',fec_ter='" + txtfecter.getText() + "', cliente_cod_cli='" + txtcodcli.getText() + "',tecnico_cod_tec='" + txtcodtec.getText() + "' WHERE cod_con='" + txtcodcon.getText() + "'");
            pps.executeUpdate();

            JOptionPane.showMessageDialog(null, "datos actualizados");
            limpiar();
            bloquear();

        } catch (SQLException ex) {
            Logger.getLogger(Newtecnico.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnActActionPerformed

