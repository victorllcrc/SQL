    void cargar(String valor) {

        //PARA CONSULTAS
        String codcli = "", codtec = "";
        String sql = "SELECT * FROM consultas WHERE CONCAT(cod_con) LIKE '%" + valor + "%'";
        conectar cc = new conectar();
        Connection cn = cc.conexion();
        Statement st;
        try {

            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            rs.next();

            txtcodcon.setText(rs.getString("cod_con"));
            txtfecini.setText(rs.getString("fec_rev"));
            txtfecter.setText(rs.getString("fec_ter"));
            txtpro.setText(rs.getString("pro_con"));
            txtsol.setText(rs.getString("sol_con"));
            txtcodcli.setText(rs.getString("cliente_cod_cli"));
            txtcodtec.setText(rs.getString("tecnico_cod_tec"));
            codcli = rs.getString("cliente_cod_cli");
            codtec = rs.getString("tecnico_cod_tec");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

        //PARA CLIENTES
        sql = "SELECT * FROM clientes WHERE CONCAT(cod_cli) LIKE '%" + codcli + "%'";
        try {

            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            rs.next();

            txtnomcli.setText(rs.getString("nom_cli"));
            txtapecli.setText(rs.getString("ape_cli"));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

        //PARA TECNICOS
        sql = "SELECT * FROM tecnicos WHERE CONCAT(cod_tec) LIKE '%" + codtec + "%'";
        try {

            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            rs.next();

            txtnomtec.setText(rs.getString("nom_tec"));
            txtaptec.setText(rs.getString("ape_tec"));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }

